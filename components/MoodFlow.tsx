interface MoodFlowProps {
  visible: boolean;
  onClose: () => void;
  onSave: (mood: 'sad' | 'neutral' | 'happy') => void;
}

const { width, height } = Dimensions.get('window');

export default function MoodFlow({ visible, onClose, onSave }: MoodFlowProps) {
  const [selectedMood, setSelectedMood] = useState<'sad' | 'neutral' | 'happy' | null>(null);

  const handleMoodSelect = (mood: 'sad' | 'neutral' | 'happy') => {
    setSelectedMood(mood);
  };

  const handleSave = () => {
    if (selectedMood) {
      onSave(selectedMood);
      setSelectedMood(null);
    }
  };

  const handleClose = () => {
    setSelectedMood(null);
    onClose();
  };

  const getMoodColor = (mood: 'sad' | 'neutral' | 'happy') => {
    switch (mood) {
      case 'sad':
        return '#FF6B6B';
      case 'neutral':
        return '#FFD93D';
      case 'happy':
        return '#6BCF7F';
      default:
        return '#E8E8E8';
    }
  };

  const getMoodSvg = (mood: 'sad' | 'neutral' | 'happy') => {
    switch (mood) {
      case 'sad':
        return faceSadSvg;
      case 'neutral':
        return faceNeutralSvg;
      case 'happy':
        return faceHappySvg;
      default:
        return faceHappySvg;
    }
  };

  return (
    <Modal
      visible={visible}
      animationType="slide"
      presentationStyle="pageSheet"
      onRequestClose={handleClose}
    >
      <View style={styles.container}>
        <View style={styles.header}>
          <TouchableOpacity onPress={handleClose} style={styles.closeButton}>
            <Text style={styles.closeButtonText}>←</Text>
          </TouchableOpacity>
          <TouchableOpacity onPress={handleClose} style={styles.xButton}>
            <Text style={styles.xButtonText}>✕</Text>
          </TouchableOpacity>
        </View>

        <View style={styles.content}>
          <Text style={styles.title}>Come ti senti ora?</Text>
          
          <View style={styles.moodContainer}>
            {(['sad', 'neutral', 'happy'] as const).map((mood) => (
              <TouchableOpacity
                key={mood}
                style={[
                  styles.moodButton,
                  {
                    backgroundColor: selectedMood === mood ? getMoodColor(mood) : '#E8E8E8',
                  },
                ]}
                onPress={() => handleMoodSelect(mood)}
              >
                <SvgXml xml={getMoodSvg(mood)} width={50} height={50} />
              </TouchableOpacity>
            ))}
          </View>

          <TouchableOpacity
            style={[
              styles.saveButton,
              { opacity: selectedMood ? 1 : 0.5 },
            ]}
            onPress={handleSave}
            disabled={!selectedMood}
          >
            <Text style={styles.saveButtonText}>SALVA</Text>
          </TouchableOpacity>
        </View>
      </View>
    </Modal>
  );
}

